/**
 * FineGuide Custom Styles
 */
function fineguide_enqueue_styles() {
    // Enqueue Google Fonts: Figtree and Nunito
    wp_enqueue_style('google-fonts', 'https://fonts.googleapis.com/css2?family=Figtree:wght@400;500;600;700&family=Nunito:wght@400;500;600;700&display=swap', array(), null);
    
    // Enqueue custom CSS
    wp_enqueue_style('fineguide-custom', get_template_directory_uri() . '/assets/css/fineguide-custom.css', array(), '1.0.1');
    
    // Enqueue Odys-inspired blog layout CSS
    wp_enqueue_style('odys-inspired', get_template_directory_uri() . '/assets/css/odys-inspired.css', array(), '1.0.0');
    
    // Enqueue Odys layout adjustments CSS
    wp_enqueue_style('odys-layout-adjustments', get_template_directory_uri() . '/assets/css/odys-layout-adjustments.css', array(), '1.0.0');
    
    // Enqueue Odys layout fixes CSS
    wp_enqueue_style('odys-layout-fixes', get_template_directory_uri() . '/assets/css/odys-layout-fixes.css', array(), '1.0.0');
    
    // Enqueue Odys single-column CSS
    wp_enqueue_style('odys-single-column', get_template_directory_uri() . '/assets/css/odys-single-column.css', array(), '1.0.0');
    
    // Enqueue FineGuide header CSS
    wp_enqueue_style('fineguide-header', get_template_directory_uri() . '/assets/css/fineguide-header.css', array(), '1.0.0');
    
    // Enqueue FineGuide header override CSS (with highest priority to override any conflicting styles)
    wp_enqueue_style('fineguide-header-override', get_template_directory_uri() . '/assets/css/fineguide-header-override.css', array(), '1.0.0');
    
    // Enqueue Blog Post Fixes CSS
    wp_enqueue_style('blog-post-fixes', get_template_directory_uri() . '/assets/css/blog-post-fixes.css', array(), '1.0.0');
}
add_action('wp_enqueue_scripts', 'fineguide_enqueue_styles', 20);

/**
 * Replace the default Astra header with our custom FineGuide header
 */
function fineguide_custom_header() {
    // Remove the default Astra header
    remove_action('astra_header', 'astra_header_markup');
    
    // Add our custom FineGuide header
    add_action('astra_header', 'fineguide_header_markup');
}
add_action('wp', 'fineguide_custom_header');

/**
 * Output the FineGuide header markup
 */
function fineguide_header_markup() {
    get_template_part('template-parts/header/fineguide-header');
}

/**
 * Add FineGuide logo to the header
 */
function fineguide_custom_logo() {
    // Add custom logo support
    add_theme_support('custom-logo', array(
        'height'      => 60,
        'width'       => 200,
        'flex-height' => true,
        'flex-width'  => true,
    ));
    
    // If no custom logo is set, use the FineGuide logo
    if (!has_custom_logo()) {
        add_action('astra_site_identity', 'fineguide_add_logo_before_site_title', 5);
    }
}
add_action('after_setup_theme', 'fineguide_custom_logo', 20);

/**
 * Add FineGuide logo before site title
 */
function fineguide_add_logo_before_site_title() {
    echo '<a href="' . esc_url(home_url('/')) . '" class="custom-logo-link" rel="home" aria-current="page">';
    echo '<img src="' . esc_url(get_template_directory_uri() . '/assets/images/fineguide/logo.svg') . '" class="custom-logo" alt="FineGuide Logo" width="50" height="50">';
    echo '</a>';
}

/**
 * Add default featured image for posts that don't have one
 */
function fineguide_default_featured_image($html, $post_id, $post_thumbnail_id, $size, $attr) {
    // If there's already a post thumbnail, return it
    if (!empty($html)) {
        return $html;
    }
    
    // Get the default image URL
    $default_image = get_template_directory_uri() . '/assets/images/fineguide/default-post.jpg';
    
    // Create the default image HTML
    $html = '<img src="' . esc_url($default_image) . '" alt="' . esc_attr(get_the_title($post_id)) . '" class="wp-post-image default-featured-img" />';
    
    return $html;
}
add_filter('post_thumbnail_html', 'fineguide_default_featured_image', 10, 5);

/**
 * Add a class to posts without content
 */
function fineguide_post_classes($classes, $class, $post_id) {
    // Check if the post has content
    $post = get_post($post_id);
    $content = $post->post_content;
    
    // If the post has no content or very little content, add a class
    if (empty($content) || strlen(strip_tags($content)) < 50) {
        $classes[] = 'no-content';
    }
    
    return $classes;
}
add_filter('post_class', 'fineguide_post_classes', 10, 3);

